static uint8_t s_font[59][7];

static uint8_t reverse5(uint8_t v) {
    uint8_t r = 0;
    for (int i = 0; i < 5; i++) {
        if (v & (1 << i)) r |= (1 << (4 - i));
    }
    return r;
}

